                        <p>Since its inspection in the 1950, the majority of companies have implemented a forecast driven MRP and DRP planning and execution model that now comes as standard with most ERP systems. However we now live in a VUCA (Volatile, Uncertain, Complex, Ambiguous) world and this de facto planning solution that was conceived over 60 years ago is not delivering the goods anymore. Companies constantly oscillate from having too much inventory aimed at protecting customer service to having to little inventories, and thus poor service levels, driven by the need to save money.</p>
                        <p>By combining MRP and DRP with the flow based methodologies of lean, theory of constraints and six sigma, a new more relevant approach for today’s VUCA world has been developed called demand driven MRP or DDMRP for short. Companies that have adopted DDMRP have seen service rate of close to 100% while at the same time reductions of inventory of between 15 to 65%. <br/> DDMRP is fast becoming the new engine of supply chain planning and execution. In order to maximise the performance of this new supply chain planning and execution engine a new operating model has been developed called Demand Driven Sales & Operations Planning or DDS&OP. DDS&OP controls the parameters of DDMRP and allow the organisations to transform from a cost-centric enterprise to a flow-focused enterprise. <br/> At B2Wise we have embraced these new methodologies and are dedicated to building new technologies and implementation methodologies to support both DDMRP and DDS&OP.</p>
